package week1.day2;

public class ExampleSwitchCase {

	public static void main(String[] args) {
	
		int j = 4;

		switch (j) {
		case 0:
			System.out.println(" The value of j is " + j);
			break;
		case 1:
			System.out.println(" The value of j is " + j);
			break;
		case 2:
			System.out.println(" The value of j is " + j);
			break;
		case 3:
			System.out.println(" The value of j is " + j);
			break;
		default:
			System.out.println(" There is no case statement for value " + j);

		}

	}

}