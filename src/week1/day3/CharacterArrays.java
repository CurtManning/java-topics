package week1.day3;

public class CharacterArrays {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		char c = 'a';
		System.out.printf("%c%n", c);
		char[] arr = { 'a', 'b', 'c', 'd' };
		System.out.printf("*****\n");
		
		for (char ch : arr) {
			System.out.printf("%c%n", ch);
		}

	}

}
